#include <stdio.h>
#include <stdint.h>
#include <interfaces/keyboard.h>

extern keyboard_t shellkeyq_get();
extern keyboard_t sdl_getKeys();

void kbd_init()
{
}

keyboard_t kbd_getKeys() {
    keyboard_t keys = 0;

    //this pulls in emulated keypresses from the command shell
    keys |= shellkeyq_get();
    keys |= sdl_getKeys();
    
    return keys;
}

